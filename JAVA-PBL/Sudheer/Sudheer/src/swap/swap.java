package swap;

public class swap {
	public swap(int a,int b) {
		System.out.println("Before Swapping: Values in the variables\n");
		System.out.println("a= "+a+"\nb= "+b+"\n");
		int z=a;
		a=b;
		b=z;
		System.out.println("After Swapping: Values in the variables\n");
		System.out.println("a= "+a+"\nb= "+b+"\n");
	}
	
	public swap(double a,double b) {
		System.out.println("Before Swapping: Values in the variables\n");
		System.out.println("a= "+a+"\nb= "+b+"\n");
		double z=a;
		a=b;
		b=z;
		System.out.println("After Swapping: Values in the variables\n");
		System.out.println("a= "+a+"\nb= "+b+"\n");
	}
}
